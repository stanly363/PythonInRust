#[derive(Debug, PartialEq, Clone)]
pub enum Token {
    Identifier(String),
    Number(i64),
    Float(f64),
    StringLiteral(String), // new token for strings
    Equals,
    Minus,
    Plus,
    Star,
    Slash,
    DoubleSlash,
    DoubleStar,
    Greater,
    Less,
    Comma,
    Print,
    Def,
    If,
    Else,
    Return,
    For,
    While,
    In,
    Range,
    OpenParen,
    CloseParen,
    Colon,
    Newline,
    Indent,
    Dedent,
    Unknown(char),
}
